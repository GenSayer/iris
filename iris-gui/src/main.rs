impl eframe::App for App {
    fn update(&mut self, ctx: &egui::Context, _frame: &mut eframe::Frame) {
        self.handle_events(ctx);
        self.maybe_autosave();

        // Intercept a clean exit (window close / File→Quit) to fold any pending
        // CHD `.diff.chd` back into its base first ("Synchronizing disks…"). On
        // the first close request with a pending sync we kick off the worker,
        // show the modal, and cancel the close; the real close happens on
        // Evt::SyncDone. `sync_then_close` latches so we don't re-intercept the
        // final close (by then the machine is gone and chd_sync_pending is stale).
        if ctx.input(|i| i.viewport().close_requested()) {
            if !self.sync_then_close && self.emu.has_pending_chd_sync() {
                self.sync_then_close = true;
                self.syncing = Some(SyncJob { disk: 0, total: 0, fraction: 0.0 });
                self.emu.send(Cmd::SyncDisks);
                ctx.send_viewport_cmd(ViewportCommand::CancelClose);
            } else if self.syncing.is_some() {
                ctx.send_viewport_cmd(ViewportCommand::CancelClose); // still syncing — stay alive
            }
        }

        // F11 toggles fullscreen — except Ctrl+Alt+F11, which is reserved as the
        // way to send a real F11 keystroke through to the guest (plain F11 never
        // reaches IRIX because this toggle eats it). input::pump forwards that
        // chord to the guest while captured.
        if ctx.input(|i| i.key_pressed(egui::Key::F11) && !(i.modifiers.ctrl && i.modifiers.alt)) {
            self.fullscreen = !self.fullscreen;
            ctx.send_viewport_cmd(ViewportCommand::Fullscreen(self.fullscreen));
        }

        // Ctrl + / Ctrl - / Ctrl 0 zoom controls (helps on Linux where egui's
        // default text size can look small on HiDPI / fractional-scale Wayland).
        let (zoom_in, zoom_out, zoom_reset) = ctx.input(|i| (
            i.modifiers.command && (i.key_pressed(egui::Key::Plus) || i.key_pressed(egui::Key::Equals)),
            i.modifiers.command && i.key_pressed(egui::Key::Minus),
            i.modifiers.command && i.key_pressed(egui::Key::Num0),
        ));
        if zoom_in    { self.prefs.ui_scale = (self.prefs.ui_scale + 0.1).min(UI_SCALE_MAX); ctx.set_zoom_factor(self.prefs.ui_scale); }
        if zoom_out   { self.prefs.ui_scale = (self.prefs.ui_scale - 0.1).max(UI_SCALE_MIN); ctx.set_zoom_factor(self.prefs.ui_scale); }
        if zoom_reset { self.prefs.ui_scale = settings::UI_SCALE_DEFAULT; ctx.set_zoom_factor(self.prefs.ui_scale); }
        // Any UI-zoom change re-fits the window so the controls grow it instead
        // of squeezing the (decoupled) VM screen.
        if zoom_in || zoom_out || zoom_reset { self.pending_fb_snap = true; }

        // The control column lives on the left, always visible (even in
        // fullscreen) — the VM screen sits to its right and never hides it.
        egui::SidePanel::left("control_panel")
            .resizable(false)
            .exact_width(186.0)
            .show(ctx, |ui| self.control_panel(ui, ctx));
        self.network_check_window(ctx);

        // Config editor placement depends on whether a machine is running:
        //  - RUNNING: a resizable right side panel, so you can edit alongside
        //    the live emulator screen (which stays visible in the centre).
        //  - IDLE: it takes the WHOLE central area instead (below), hiding the
        //    welcome/info screen — no cramped split when there's nothing to
        //    watch. The toolbar's "Edit config…" toggle drives both.
        let config_in_side_panel = self.show_config_editor && self.emu.is_running();
        egui::SidePanel::right("config_editor")
            .resizable(true)
            .default_width(420.0)
            .show_animated(ctx, config_in_side_panel, |ui| self.config_editor_panel(ui));

        // Zero the central panel's inner margin so the emulated display reaches
        // the window edges — every reclaimed pixel makes the (tall, 5:4) picture
        // a little bigger. Keep the dark panel fill so the aspect-ratio
        // letterbox bars stay black.
        let central_frame = egui::Frame::central_panel(&ctx.style())
            .inner_margin(egui::Margin::ZERO);
        egui::CentralPanel::default().frame(central_frame).show(ctx, |ui| {
            if self.show_config_editor && !self.emu.is_running() {
                // Idle + editing: config fills the whole pane (welcome hidden).
                // A small margin gives it breathing room (the central frame is
                // edge-to-edge for the framebuffer).
                input::force_release(ui.ctx(), &mut self.input_state);
                egui::Frame::none()
                    .inner_margin(egui::Margin::symmetric(10.0, 8.0))
                    .show(ui, |ui| self.config_editor_panel(ui));
            } else if self.emu.is_running() {
                self.framebuffer_panel(ui);
            } else {
                // Size the window for the standard 1280×1024 display at the
                // chosen VM scale before the user sees it. Runs on every launch
                // (window size isn't persisted), so the window always opens at
                // the configured scale. Wait until the monitor size is known
                // before consuming the flag.
                if self.pending_launcher_fit
                    && ui.ctx().input(|i| i.viewport().monitor_size).is_some()
                {
                    Self::snap_window_to_fb(ui.ctx(), egui::vec2(1280.0, 1024.0), ui.available_size(), self.prefs.vm_scale);
                    self.pending_launcher_fit = false;
                }
                // Emulator not running: make sure a leftover mouse capture is
                // released so the host cursor isn't stuck hidden/locked.
                input::force_release(ui.ctx(), &mut self.input_state);
                self.welcome_panel(ui);
            }
        });

        // Rename-machine modal: a text box + OK/Cancel. The buffer is App state
        // (`rename_buffer`), so typed input persists across frames — a text box
        // inside the menu can't, because the menu closure resets it each frame.
        let mut rename_result: Option<Option<String>> = None; // outer Some=action; inner Some=new name, None=cancel
        if let Some(buf) = &mut self.rename_buffer {
            egui::Window::new("Rename machine")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.label("New machine name:");
                    let resp = ui.add(egui::TextEdit::singleline(buf).desired_width(260.0));
                    resp.request_focus();
                    let entered = resp.lost_focus() && ui.input(|i| i.key_pressed(egui::Key::Enter));
                    ui.add_space(6.0);
                    ui.horizontal(|ui| {
                        let ok = ui.add(egui::Button::new("OK").fill(Color32::from_rgb(60, 90, 140))).clicked();
                        if (ok || entered) && !buf.trim().is_empty() {
                            rename_result = Some(Some(buf.clone()));
                        }
                        if ui.button("Cancel").clicked() { rename_result = Some(None); }
                    });
                });
        }
        match rename_result {
            Some(Some(name)) => { self.apply_rename(&name); self.rename_buffer = None; }
            Some(None)       => { self.rename_buffer = None; }
            None             => {}
        }

        // New machine dialog.
        self.new_machine.show(ctx);
        if let Some(result) = self.new_machine.take_result() {
            let name = self.prefs.unique_name(&result.name);
            self.prefs.machines.insert(name.clone(), result.cfg.clone());
            self.prefs.active_machine = Some(name.clone());
            self.cfg = result.cfg;
            self.cfg_path = None;
            self.flush_machine();
            self.toast(format!("created machine '{name}'"));
        }

        // Create blank disk dialog.
        self.create_disk.show(ctx);
        if let Some(result) = self.create_disk.take_result() {
            let path_str = result.path.to_string_lossy().into_owned();
            self.cfg.scsi.insert(result.scsi_id, iris::config::ScsiDeviceConfig {
                path: path_str.clone(), discs: vec![], cdrom: false,
                overlay: false, scratch: false, size_mb: None,
            });
            self.mark_dirty();
            self.toast(format!("created {path_str} and attached at scsi{}", result.scsi_id));
        }

        // Live host-camera test window.
        self.camera_test_window(ctx);

        // In-app IRIX serial-console viewer.
        self.serial_console_window(ctx);

        // Help → "How camera & networking work" explainer.
        self.help_info_window(ctx);
        self.license_window(ctx);
        self.privacy_window(ctx);

        // Help → "Mount the shared folder in IRIX" — the NFS mount command.
        self.nfs_help_window(ctx);

        // "Synchronizing disks…" modal during the exit-time CHD fold-back.
        self.sync_modal(ctx);

        // "Discard changes?" confirmation for a COW roll-back.
        self.cow_discard_modal(ctx);

        // Safe-stop confirmation modal.
        let mut close_modal = false;
        let mut do_force = false;
        let mut do_halt  = false;
        if let Some(modal) = &self.stop_modal {
            egui::Window::new("Confirm stop")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.label(RichText::new("Stopping now may corrupt the disk image:").strong());
                    for line in &modal.lines { ui.label(format!("• {line}")); }
                    ui.add_space(8.0);
                    ui.horizontal(|ui| {
                        if ui.button("Cancel").clicked() { close_modal = true; }
                        if ui.button("Send IRIX halt").clicked() { do_halt = true; close_modal = true; }
                        if ui.add(egui::Button::new(RichText::new("Force stop").color(Color32::WHITE))
                            .fill(Color32::from_rgb(140, 40, 40))).clicked()
                        {
                            do_force = true; close_modal = true;
                        }
                    });
                });
        }
        if close_modal { self.stop_modal = None; }
        if do_force { self.emu.send(Cmd::Stop); }
        if do_halt  {
            // Type "halt\n" at the IRIX serial console in-process (see
            // EmulatorHandle / Machine::inject_serial_console). This used to
            // open a loopback TCP client to 127.0.0.1:8881; doing it in-process
            // means clean shutdown no longer depends on the serial server
            // socket (which the macOS App Sandbox would otherwise gate behind
            // the network.server entitlement). IRIX takes a few seconds to shut
            // down cleanly; the user can hit Stop once the PROM "halted"
            // message appears.
            self.emu.send(Cmd::HaltIrix);
            self.toast("sent 'halt' to IRIX — wait for shutdown, then Stop");
        }

        // Confirm switching from a custom PROM back to the built-in image.
        if self.confirm_embedded_prom {
            let prom = self.cfg.prom.clone();
            let mut close = false;
            let mut do_clear = false;
            egui::Window::new("Use embedded PROM?")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.label(RichText::new("Replace the selected PROM with IRIS's built-in PROM?").strong());
                    ui.label(RichText::new(&prom).weak());
                    ui.add_space(8.0);
                    ui.horizontal(|ui| {
                        if ui.button("Cancel").clicked() { close = true; }
                        if ui.button("Use embedded PROM").clicked() { do_clear = true; close = true; }
                    });
                });
            if do_clear {
                self.cfg.prom.clear();
                self.mark_dirty();
                self.toast("using embedded PROM");
            }
            if close { self.confirm_embedded_prom = false; }
        }

        // Networking sanity-check override modal.
        if let Some(modal) = &self.net_sanity_modal {
            let reason = modal.reason.clone();
            let suggestion = modal.suggestion.clone();
            let revert_to = modal.revert_to.clone();
            let mut close = false;
            let mut do_revert = false;
            let mut do_suggest = false;
            egui::Window::new("Check networking configuration")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.set_max_width(420.0);
                    ui.label(RichText::new(
                        "This networking configuration does not appear to be valid, please double-check:")
                        .strong());
                    ui.label(format!("- {reason}"));
                    ui.label(RichText::new(
                        "The defaults are chosen to just work; an unusual subnet can leave the Indy \
                         without internet access.").weak());
                    ui.add_space(8.0);
                    ui.horizontal(|ui| {
                        if ui.button("Cancel").clicked() { do_revert = true; close = true; }
                        if ui.add(egui::Button::new(format!("Use {suggestion}"))
                            .fill(Color32::from_rgb(60, 90, 140))).clicked()
                        {
                            do_suggest = true; close = true;
                        }
                        if ui.add(egui::Button::new("Override Sanity Checks")
                            .fill(Color32::from_rgb(140, 90, 40))).clicked()
                        {
                            close = true;
                        }
                    });
                });
            if do_revert  { self.cfg.nat_subnet = revert_to;          self.mark_dirty(); }
            if do_suggest { self.cfg.nat_subnet = Some(suggestion);   self.mark_dirty(); }
            if close { self.net_sanity_modal = None; }
        }

        // PCAP capture-permission prompt (button-raised or auto-raised on a
        // permission-denied capture). "Enable" runs the platform privilege flow;
        // "Not now" just dismisses — the guest keeps running on NAT-less PCAP
        // (i.e. no networking) until the user enables capture or switches to NAT.
        if self.pcap_perm_modal {
            let mut do_enable = false;
            let mut close = false;
            egui::Window::new("Enable packet capture")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.set_max_width(440.0);
                    ui.label(RichText::new(
                        "PCAP (bridged) networking needs permission to capture on a real \
                         interface, which IRIS doesn't have yet.").strong());
                    ui.add_space(4.0);
                    ui.label(RichText::new(capture_access::permission_hint()).weak());
                    ui.add_space(8.0);
                    ui.horizontal(|ui| {
                        if ui.add(egui::Button::new("Enable packet capture…")
                            .fill(Color32::from_rgb(60, 90, 140))).clicked()
                        {
                            do_enable = true;
                        }
                        if ui.button("Not now").clicked() { close = true; }
                    });
                });
            if do_enable { self.run_enable_packet_capture(); } // also clears the modal
            if close { self.pcap_perm_modal = false; }
        }

        // Missing-disk modal.
        enum MissingChoice { None, Cancel, Detach, EditDisks }
        let mut choice = MissingChoice::None;
        if let Some(modal) = &self.missing_modal {
            egui::Window::new("Missing disk image(s)")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.label(RichText::new("These SCSI image files are missing or can't be read:").strong());
                    for m in &modal.missing {
                        let kind = if m.cdrom { "CD-ROM" } else { "HDD" };
                        ui.label(format!("• scsi{} ({kind}): {}", m.id, m.path));
                    }
                    ui.add_space(8.0);
                    if cfg!(feature = "appstore") {
                        // Under the sandbox an existing file may be unreadable
                        // because the app's access to it lapsed — re-selecting it
                        // re-grants access and stores a bookmark for next launch.
                        ui.label(RichText::new(
                            "If a file exists but still shows here, macOS revoked access \
                             after a previous session. Open the Disks tab and re-select it \
                             to restore access.").weak());
                        ui.add_space(4.0);
                    }
                    ui.label(RichText::new(
                        "iris would terminate the process if started in this state. \
                         Choose how to proceed:").weak());
                    ui.add_space(4.0);
                    ui.horizontal(|ui| {
                        if ui.button("Cancel").clicked() { choice = MissingChoice::Cancel; }
                        if ui.button("Edit Disks tab").clicked() { choice = MissingChoice::EditDisks; }
                        if ui.add(egui::Button::new("Detach missing & start")
                            .fill(Color32::from_rgb(60, 90, 140))).clicked()
                        {
                            choice = MissingChoice::Detach;
                        }
                    });
                });
        }
        match choice {
            MissingChoice::None => {}
            MissingChoice::Cancel => { self.missing_modal = None; }
            MissingChoice::EditDisks => {
                // Switching the tab alone is invisible if the config editor
                // side panel is collapsed (its default state) — open it too,
                // or the button appears to do nothing.
                self.tab = config_ui::Tab::Disks;
                self.show_config_editor = true;
                self.missing_modal = None;
            }
            MissingChoice::Detach => {
                let ids: Vec<u8> = self.missing_modal.as_ref()
                    .map(|m| m.missing.iter().map(|d| d.id).collect())
                    .unwrap_or_default();
                self.missing_modal = None;
                self.detach_and_start(&ids);
            }
        }

        // CHD folder-grant modal (App Store): attached CHDs sit in folders we
        // can't write, so their on-exit fold would be denied. Offer to grant
        // each folder, or start anyway and forgo compaction.
        // `Grant` is built only in the sandbox branch, `OpenPrivacy`/`Recheck`
        // only in the notarized branch — so one set is always cfg-dead.
        #[allow(dead_code)]
        enum ChdGrantChoice { None, Cancel, StartAnyway, Grant(String), OpenPrivacy, Recheck }
        let mut chd_choice = ChdGrantChoice::None;
        if let Some(modal) = &self.chd_grant_modal {
            egui::Window::new("Grant folder access to compact disks")
                .collapsible(false)
                .resizable(false)
                .anchor(egui::Align2::CENTER_CENTER, [0.0, 0.0])
                .show(ctx, |ui| {
                    ui.label(RichText::new("These CHD disks are in folders IRIS can't write to:").strong());
                    for d in &modal.disks {
                        ui.label(format!("• scsi{}: {}", d.id, d.path));
                    }
                    ui.add_space(8.0);
                    ui.label(RichText::new(
                        "A compressed CHD records changes in a sidecar while running, then folds \
                         them back into the disk when you quit (\"Synchronizing disks…\"). That \
                         fold needs write access to the disk's folder — not just the file — so \
                         without it the sidecar only grows and the disk never shrinks.").weak());
                    ui.add_space(6.0);
                    ui.label(RichText::new(
                        "Tip: a folder grant is recursive — IRIS gains access to everything in \
                         that folder for as long as it's granted. To keep that exposure minimal, \
                         put each disk image in its own dedicated folder and grant only that.")
                        .italics().weak());
                    ui.add_space(6.0);
                    // Sandbox (MAS): a directory security-scoped grant via the
                    // open panel conveys write access. One button per distinct
                    // folder — a recursive grant covers every disk under it.
                    #[cfg(feature = "appstore")]
                    {
                        ui.label(RichText::new("Grant the containing folder so IRIS can compact it on exit:").weak());
                        let mut dirs: Vec<&str> = modal.disks.iter().map(|d| d.dir.as_str()).collect();
                        dirs.sort_unstable();
                        dirs.dedup();
                        for dir in dirs {
                            if ui.button(format!("Grant \"{dir}\"…")).clicked() {
                                chd_choice = ChdGrantChoice::Grant(dir.to_string());
                            }
                        }
                    }
                    // Notarized macOS: the block is macOS Privacy (TCC), which a
                    // folder pick doesn't override — the user allows IRIS in
                    // System Settings, then re-checks (a relaunch may be needed
                    // for Full Disk Access to take effect).
                    #[cfg(not(feature = "appstore"))]
                    {
                        ui.label(RichText::new(
                            "macOS Privacy & Security is blocking write access to these folders. \
                             Allow IRIS under System Settings → Privacy & Security (Files and Folders, \
                             or add it to Full Disk Access), then Re-check. You may need to quit and \
                             reopen IRIS for Full Disk Access to take effect.").weak());
                        ui.horizontal(|ui| {
                            if ui.button("Open Privacy & Security…").clicked() {
                                chd_choice = ChdGrantChoice::OpenPrivacy;
                            }
                            if ui.button("Re-check").clicked() {
                                chd_choice = ChdGrantChoice::Recheck;
                            }
                        });
                    }
                    ui.add_space(4.0);
                    ui.horizontal(|ui| {
                        if ui.button("Cancel").clicked() { chd_choice = ChdGrantChoice::Cancel; }
                        if ui.add(egui::Button::new("Start without compacting")
                            .fill(Color32::from_rgb(60, 90, 140))).clicked()
                        {
                            chd_choice = ChdGrantChoice::StartAnyway;
                        }
                    });
                });
        }
        match chd_choice {
            ChdGrantChoice::None => {}
            ChdGrantChoice::Cancel => { self.chd_grant_modal = None; }
            ChdGrantChoice::StartAnyway => {
                self.chd_grant_modal = None;
                self.skip_chd_grant_check = true; // one-shot bypass
                self.start_emulator();
            }
            ChdGrantChoice::Grant(dir) => {
                self.grant_disk_folder_at(Some(&dir));
                // Re-probe: drop disks whose folder is now writable. When all are
                // satisfied, dismiss and start; otherwise leave the rest listed.
                if let Some(modal) = &mut self.chd_grant_modal {
                    modal.disks.retain(|d| !dir_writable(std::path::Path::new(&d.dir)));
                    if modal.disks.is_empty() {
                        self.chd_grant_modal = None;
                        self.start_emulator();
                    }
                }
            }
            ChdGrantChoice::OpenPrivacy => {
                // Open System Settings → Privacy & Security → Full Disk Access so
                // the user can add IRIS (the "+"). No-op off macOS.
                #[cfg(target_os = "macos")]
                {
                    let _ = std::process::Command::new("open")
                        .arg("x-apple.systempreferences:com.apple.preference.security?Privacy_AllFiles")
                        .spawn();
                }
            }
            ChdGrantChoice::Recheck => {
                // Re-probe each listed folder; drop the ones now writable (the
                // user allowed IRIS in System Settings). Mirrors the Grant path.
                if let Some(modal) = &mut self.chd_grant_modal {
                    modal.disks.retain(|d| !dir_writable(std::path::Path::new(&d.dir)));
                    if modal.disks.is_empty() {
                        self.chd_grant_modal = None;
                        self.start_emulator();
                    }
                }
            }
        }

        // The window starts hidden so the first frame(s) can fit it to the
        // monitor (the launcher fit, when not running) before it's shown —
        // avoiding a visible open-then-resize. Reveal one frame after the fit
        // settles so its resize has applied, or unconditionally after a short
        // grace period so a missing monitor size can't leave the window hidden.
        if !self.revealed {
            self.startup_frame = self.startup_frame.saturating_add(1);
            let fit_settled = !self.pending_launcher_fit;
            if (fit_settled && self.startup_frame >= 2) || self.startup_frame >= 10 {
                ctx.send_viewport_cmd(ViewportCommand::Visible(true));
                self.revealed = true;
            } else {
                // Keep frames coming while hidden so we reach the reveal.
                ctx.request_repaint();
            }
        }
    }

    fn on_exit(&mut self, _gl: Option<&eframe::glow::Context>) {
        // Make sure the latest cfg lands in `machines` before save().
        if self.cfg_dirty { self.flush_machine(); } else { let _ = self.prefs.save(); }
        // Synchronously stop the machine and join the worker, so a running
        // guest is cleaned up even if the user closed the window without
        // pressing Stop (rather than relying on Drop ordering / racing
        // process teardown). Bounded by the worker's stop timeout.
        self.emu.shutdown();
        // Release the single-instance lock so the next launch sees a clean
        // start (a crash that skips this just leaves a stale, harmless pidfile).
        single_instance::release();
    }
}

// --- platform dialogs ------------------------------------------------------
//
// We avoid `rfd` as a dependency for now to keep the dep tree slim. Use
// `zenity` / `osascript` if available; otherwise return None and let the
// caller paste a path into the recent-files / save-state fields.
fn native_open_dialog(title: &str, filters: &[(&str, &[&str])]) -> Option<PathBuf> {
    let mut d = rfd::FileDialog::new().set_title(title);
    for (name, exts) in filters { d = d.add_filter(*name, exts); }
    d.pick_file()
}
fn native_save_dialog(title: &str, filters: &[(&str, &[&str])]) -> Option<PathBuf> {
    let mut d = rfd::FileDialog::new().set_title(title);
    for (name, exts) in filters { d = d.add_filter(*name, exts); }
    d.save_file()
}

#[cfg(test)]
mod preflight_tests {
    use super::disk_readable;
    use std::io::Write;

    #[test]
    fn empty_and_missing_paths_are_not_readable() {
        assert!(!disk_readable(""));
        assert!(!disk_readable("/no/such/iris/disk/image.chd"));
    }

    #[test]
    fn an_existing_readable_file_is_readable() {
        let mut p = std::env::temp_dir();
        p.push(format!("iris_readable_{}.raw", std::process::id()));
        std::fs::File::create(&p).unwrap().write_all(b"\0\0\0\0").unwrap();
        assert!(disk_readable(&p.to_string_lossy()));
        let _ = std::fs::remove_file(&p);
    }

    // The case the sandbox actually hits: a file that exists (so Path::exists()
    // is true) but cannot be opened. Simulated with chmod 000. Skipped when the
    // test runs as root, where DAC permission checks are bypassed and the open
    // would still succeed.
    #[cfg(unix)]
    #[test]
    fn an_unreadable_but_present_file_is_not_readable() {
        use std::os::unix::fs::PermissionsExt;
        if unsafe { libc_geteuid() } == 0 {
            return; // root bypasses 000 perms; the distinction can't be shown
        }
        let mut p = std::env::temp_dir();
        p.push(format!("iris_unreadable_{}.raw", std::process::id()));
        std::fs::File::create(&p).unwrap().write_all(b"\0\0\0\0").unwrap();
        std::fs::set_permissions(&p, std::fs::Permissions::from_mode(0o000)).unwrap();
        // exists() says present, but disk_readable() must say no.
        assert!(p.exists());
        assert!(!disk_readable(&p.to_string_lossy()));
        let _ = std::fs::set_permissions(&p, std::fs::Permissions::from_mode(0o600));
        let _ = std::fs::remove_file(&p);
    }

    #[cfg(unix)]
    extern "C" {
        #[link_name = "geteuid"]
        fn libc_geteuid() -> u32;
    }
}
