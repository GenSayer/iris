/// Decode the bundled window/taskbar icon (256×256 RGBA PNG, generated by
/// `scripts/generate-icon.sh` from `iris-gui/assets/icon-original.png`).
/// Decoded with the `png` crate that iris-gui already depends on, so no extra
/// image dependency is pulled in.
fn load_icon() -> egui::IconData {
    const ICON_PNG: &[u8] = include_bytes!("../assets/icons/icon-256.png");
    let decoder = png::Decoder::new(std::io::Cursor::new(ICON_PNG));
    let mut reader = decoder.read_info().expect("decode icon PNG header");
    let mut rgba = vec![0u8; reader.output_buffer_size()];
    let info = reader.next_frame(&mut rgba).expect("decode icon PNG pixels");
    rgba.truncate(info.buffer_size());
    egui::IconData {
        rgba,
        width: info.width,
        height: info.height,
    }
}

/// Largest aspect-preserving size (egui points) that fits `avail` for a
/// framebuffer whose native pixel dimensions are `px`.
fn fb_fit_size(avail: egui::Vec2, px: egui::Vec2) -> egui::Vec2 {
    let fb_aspect = px.x / px.y;
    if avail.x / avail.y > fb_aspect {
        egui::vec2(avail.y * fb_aspect, avail.y)
    } else {
        egui::vec2(avail.x, avail.x / fb_aspect)
    }
}

/// Resolve a config path to an absolute string for display — a relative path is
/// joined to the process working directory (where the emulator actually looks
/// for it), so the user never sees a bare `scsi3.raw` with no idea where it is.
fn abs_path(p: &str) -> String {
    let path = std::path::Path::new(p);
    if path.is_absolute() || p.is_empty() {
        return p.to_string();
    }
    std::env::current_dir()
        .map(|d| d.join(path).to_string_lossy().into_owned())
        .unwrap_or_else(|_| p.to_string())
}

/// True when `scale` (device pixels per emulated pixel) is close enough to a
/// positive integer that nearest-neighbour sampling stays pixel-perfect. Off
/// an integer, bilinear filtering avoids uneven pixel doubling.
fn is_integer_scale(scale: f32) -> bool {
    let rounded = scale.round();
    rounded >= 1.0 && (scale - rounded).abs() <= 0.01
}

fn main() -> eframe::Result<()> {
    env_logger::init();
    // Prevent the iris lib from calling process::exit on guest soft-power-off
    // or CI `quit`. iris-gui never wants the embedder to die from a guest event.
    // Set this once, before any worker thread can read it.
    std::env::set_var("IRIS_NO_EXIT_ON_POWEROFF", "1");
    // Terminate a still-alive previous instance (a crash/hang or forgotten
    // copy that would otherwise keep the monitor/serial ports bound) and claim
    // the single-instance lock for ourselves.
    single_instance::acquire();
    let prefs = GuiSettings::load();
    // Re-acquire macOS sandbox access to previously user-selected files (disk
    // images, PROM, ISOs, …) before any machine can open them. No-op elsewhere.
    macos_sandbox::restore(&prefs.bookmarks);
    // Under the App Sandbox a compressed HD CHD's `.diff.chd` sidecar can't be
    // created next to the parent (its directory isn't writable). Redirect diffs
    // into our writable data dir (container-redirected under the sandbox). Off
    // the App Store build the diff stays beside the parent (no sandbox).
    #[cfg(feature = "appstore")]
    if let Some(d) = dirs::data_dir() {
        std::env::set_var("IRIS_CHD_DIFF_DIR", d.join("iris").join("chd-diffs"));
    }
    // Force the interpreter on the Mac App Store build. Cranelift (MIPS JIT and
    // the always-on REX3 draw-shader JIT) allocates executable memory with
    // mmap+mprotect, not MAP_JIT. The App Sandbox only permits MAP_JIT pages
    // (com.apple.security.cs.allow-jit is the sole code-signing entitlement MAS
    // allows; allow-unsigned-executable-memory / disable-executable-page-
    // protection are rejected by App Review), so the first JITed REX3 draw is
    // killed with SIGKILL/CODESIGNING "Invalid Page" on the REX3-Processor
    // thread — right after the emulator window opens, before any video. Set
    // once, before any worker thread (CPU / REX3) can read it.
    #[cfg(feature = "appstore")]
    std::env::set_var("IRIS_NO_JIT", "1");
    let viewport = egui::ViewportBuilder::default()
        .with_title("iris — SGI Indy emulator")
        // app_id sets the X11 WM_CLASS / Wayland app_id so the compositor can
        // match an installed .desktop/icon (icons regenerated by
        // scripts/generate-icon.sh from iris-gui/assets/icon-original.png).
        .with_app_id("iris-gui")
        .with_icon(load_icon())
        // Open large enough for the 1280×1024 display + chrome so it looks right
        // immediately; clamp to the monitor so it can't overflow a smaller
        // screen. Persisted size (once saved) takes precedence over the default.
        .with_inner_size(WINDOW_DEFAULT_SIZE)
        .with_clamp_size_to_monitor_size(true)
        // Start hidden so the first frame can fit the window to the monitor
        // (see the reveal logic in `update`) before it's shown — the window
        // then appears already at the right size instead of opening at the
        // default and visibly resizing.
        .with_visible(false);
    // Intentionally do NOT restore fullscreen on launch — the app always opens
    // windowed; fullscreen is only ever entered when the user asks (F11).
    let opts = eframe::NativeOptions {
        viewport,
        ..Default::default()
    };
    eframe::run_native(
        "iris-gui",
        opts,
        Box::new(|cc| {
            cc.egui_ctx.set_zoom_factor(prefs.ui_scale.clamp(UI_SCALE_MIN, UI_SCALE_MAX));
            Ok(Box::new(App::new(prefs)))
        }),
    )
}

